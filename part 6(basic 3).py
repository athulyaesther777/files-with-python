import pyautogui

# to perform different types of mouse clicks
# at specific coordinates on the screen

# Single click at the absolute position (100, 100) on the screen
pyautogui.click(100, 100)

# Double click at the absolute position (100, 100) on the screen
pyautogui.doubleClick(100, 100)

# Right click at the absolute position (100, 100) on the screen
pyautogui.rightClick(100, 100)

# Middle click
pyautogui.middleClick(100, 100)
